package com.itonglian.entity;

public class OfChat {

    private String chat_id;

    private String chat_name;

    private String chat_user;

    private String chat_other;

    private String chat_create_time;

    private String chat_modify_time;

    private String chat_pic;

    public String getChat_id() {
        return chat_id;
    }

    public void setChat_id(String chat_id) {
        this.chat_id = chat_id;
    }

    public String getChat_name() {
        return chat_name;
    }

    public void setChat_name(String chat_name) {
        this.chat_name = chat_name;
    }

    public String getChat_user() {
        return chat_user;
    }

    public void setChat_user(String chat_user) {
        this.chat_user = chat_user;
    }

    public String getChat_other() {
        return chat_other;
    }

    public void setChat_other(String chat_other) {
        this.chat_other = chat_other;
    }

    public String getChat_create_time() {
        return chat_create_time;
    }

    public void setChat_create_time(String chat_create_time) {
        this.chat_create_time = chat_create_time;
    }

    public String getChat_modify_time() {
        return chat_modify_time;
    }

    public void setChat_modify_time(String chat_modify_time) {
        this.chat_modify_time = chat_modify_time;
    }

    public String getChat_pic() {
        return chat_pic;
    }

    public void setChat_pic(String chat_pic) {
        this.chat_pic = chat_pic;
    }
}
